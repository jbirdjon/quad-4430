import turtle

def draw():
    window = turtle.Screen()
    window.bgcolor("blue")
    kite()
    quadrilateral()
    trapezoid()
    square()
    rectangle()
    isosceles_trapezoid()
    rhombus()
    parallelogram()
    window.exitonclick()

def square():
    t = turtle.Turtle()
    t.setheading(90)
    t.forward(100)
    t.setheading(180)
    t.forward(100)
    t.setheading(270)
    t.forward(100)
    t.setheading(360)
    t.forward(100)


def rectangle():
    t = turtle.Turtle()
    t.color("blue")
    t.goto(0,-100)
    t.color("black")
    t.setheading(90)
    t.forward(50)
    t.setheading(180)
    t.forward(100)
    t.setheading(270)
    t.forward(50)
    t.setheading(360)
    t.forward(100)

def rhombus():
    t = turtle.Turtle()
    t.color("blue")
    t.goto(0,-1)
    t.goto(200,-1)
    t.color("black")
    t.setheading(40)
    t.forward(70)
    t.setheading(180)
    t.forward(70)
    t.setheading(220)
    t.forward(70)
    t.setheading(360)
    t.forward(70)

def parallelogram():
    t = turtle.Turtle()
    t.color("blue")
    t.goto(0,-4)
    t.goto(200,-4)
    t.goto(200,-5)
    t.goto(200,-200)
    t.color("black")
    t.setheading(100)
    t.forward(70)
    t.setheading(170)
    t.forward(70)
    t.setheading(280)
    t.forward(70)
    t.setheading(350)
    t.forward(70)

def kite():
    t = turtle.Turtle()
    t.penup()
    t.goto(-250, 200)
    t.pendown()
    t.goto(-200, 270)
    t.goto(-150, 200)
    t.goto(-200, 160)
    t.goto(-250, 200)

def quadrilateral():
    t = turtle.Turtle()
    t.penup()
    t.goto(-280, 20)
    t.pendown()
    t.goto(-265, 120)
    t.goto(-185, 105)
    t.goto(-170, 35)
    t.goto(-280, 20)

def trapezoid():
    t = turtle.Turtle()
    t.penup()
    t.goto(-320, -130)
    t.pendown()
    t.goto(-305, -50)
    t.goto(-225, -50)
    t.goto(-180, -130)
    t.goto(-320, -130)

def isosceles_trapezoid():
    t = turtle.Turtle()
    t.penup()
    t.goto(140, 120)
    t.pendown()
    t.goto(175, 200)
    t.goto(245, 200)
    t.goto(280, 120)
    t.goto(140, 120)

draw()